st.write(rand_row)

x_input_proc = preprocess(rand_row)
x_input_proc = x_input_proc.reindex(columns=X.columns, fill_value=0)
x_input_proc = x_input_proc.fillna(0)

# -------------------------------
# ‡∏ó‡∏≥‡∏ô‡∏≤‡∏¢‡∏ú‡∏•
# -------------------------------
st.subheader("üîç ‡∏ú‡∏•‡∏Å‡∏≤‡∏£‡∏ó‡∏≥‡∏ô‡∏≤‡∏¢")
